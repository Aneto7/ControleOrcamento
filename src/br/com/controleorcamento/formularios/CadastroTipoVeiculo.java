    private void tabelaTipoVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaTipoVeiculosMouseClicked
        if (tabelaTipoVeiculos.getSelectedRow() != -1) {
            lID.setText(tabelaTipoVeiculos.getValueAt(tabelaTipoVeiculos.getSelectedRow(), 0).toString());
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = ConexaoMySQL.getConexaoMySQL();
                java.sql.Statement st = conn.createStatement();
                st.executeQuery("SELECT * FROM `tipoveiculo` WHERE `ID` = " + this.lID.getText());
                ResultSet rs = st.getResultSet();

                while (rs.next()) {
                    this.tBoxModelo.setText(rs.getString("MODELO"));
                    this.tBoxTipo.setText(rs.getString("TIPO"));
                }
                ConexaoMySQL.FecharConexao();
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, e);
                ConexaoMySQL.FecharConexao();
            }
        }
    }//GEN-LAST:event_tabelaTipoVeiculosMouseClicked

