    private void bExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirActionPerformed
        if (lID.getText() == "") {
            JOptionPane.showMessageDialog(this, "Selecione um tipo de veículo clicando na tabela");
            return;
        }
        TipoVeiculo tipo = new TipoVeiculo();
        TipoVeiculoExcluir texc = new TipoVeiculoExcluir();

        tipo.setId(Integer.parseInt(lID.getText()));

        texc.TipoVeiculoExcluir(tipo);
        limpar();
        filtrar();
        JOptionPane.showMessageDialog(this, "Veículo excluído com sucesso");
    }//GEN-LAST:event_bExcluirActionPerformed

