    private void bAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtualizarActionPerformed

        Combustivel combus = new Combustivel();
        CombustivelAtualizar combusatu = new CombustivelAtualizar();

        SimpleDateFormat fordata = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date datacom = fordata.parse((String) this.fTextData.getText());
            SimpleDateFormat forma = new SimpleDateFormat("yyyy-MM-dd");
            String datafor = (String) forma.format(datacom);
            combus.setDataab(datafor);

        } catch (ParseException ex) {
            Logger.getLogger(CadastroCombustivel.class.getName()).log(Level.SEVERE, null, ex);
        }
        String kmtransformado = ((String) this.fTextKm.getText());
        kmtransformado = kmtransformado.replace(".", "");
        kmtransformado = kmtransformado.replace(",", ".");

        String valortransformado = ((String) this.fTextValor.getText());
        valortransformado = valortransformado.replace(".", "");
        valortransformado = valortransformado.replace(",", ".");

        String litrostransformado = ((String) this.fTextLitros.getText());
        litrostransformado = litrostransformado.replace(".", "");
        litrostransformado = litrostransformado.replace(",", ".");

        combus.setId(Integer.parseInt(lID.getText()));
        combus.setPlaca((String) cBoxPlaca.getSelectedItem());
        combus.setCartao((String) cBoxCartao.getSelectedItem());
        combus.setValor(Double.parseDouble(valortransformado));
        combus.setKm(Double.parseDouble(kmtransformado));
        combus.setLitros(Double.parseDouble(litrostransformado));
        combus.setCentro(tBoxCentroCusto.getText());
        combus.setCondutor((String) cBoxCondutor.getSelectedItem());

        combusatu.CombustivelAtualizar(combus);
        limpar();
        filtrar();
        JOptionPane.showMessageDialog(this, "Combustivel atualizado com sucesso");
    }//GEN-LAST:event_bAtualizarActionPerformed

