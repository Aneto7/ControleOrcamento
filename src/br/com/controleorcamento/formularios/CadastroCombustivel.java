    public void filtrar() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaCombustivel.getModel();
        tabelaCombustivel.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT * FROM `combustivel` WHERE `PLACA` LIKE '%"
                    + this.cBoxPlaca.getSelectedItem() + "%' AND `CARTAO` LIKE '%"
                    + this.cBoxCartao.getSelectedItem() + "%' AND `CONDUTOR` LIKE '%"
                    + this.cBoxCondutor.getSelectedItem() + "%' ORDER BY `PLACA`");
            ResultSet rs = st.getResultSet();

            while (rs.next()) {
                SimpleDateFormat formatado = new SimpleDateFormat("dd/MM/yyyy");
                String data = formatado.format(rs.getDate("DATA"));

                modelo.addRow(new Object[]{
                    rs.getInt("ID"),
                    rs.getString("PLACA"),
                    rs.getString("CARTAO"),
                    data,
                    rs.getDouble("VALOR"),
                    rs.getDouble("LITROS"),
                    rs.getDouble("KM"),
                    rs.getString("CONDUTOR"),});
            }
            somarValoresdaTabela();
            ConexaoMySQL.FecharConexao();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }
    }

