    public void somarValoresdaTabela() {
        DecimalFormat df = new DecimalFormat("###,###,###,###.##");
        df.setRoundingMode(RoundingMode.UP);

        double mediaabastecimento = 0;
        double acumuladoabastecimento = 0;
        double quantidadeabastecimento = 0;
        for (int i = 0; i <= tabelaCombustivel.getRowCount() - 1; i++) {
            acumuladoabastecimento += Double.parseDouble(tabelaCombustivel.getValueAt(i, 4).toString());
            quantidadeabastecimento = i;
        }
        mediaabastecimento = acumuladoabastecimento / quantidadeabastecimento;
        lMediaCombustivel.setText(String.valueOf(df.format(mediaabastecimento)));
        lAcumuladoCombustivel.setText(String.valueOf(df.format(acumuladoabastecimento)));
    }

