    public void filtrar() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaVeiculos.getModel();
        tabelaVeiculos.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT * FROM `filial` WHERE `FILIAL` LIKE '%"
                    + this.tBoxFilial.getText() + "%' AND `REGIAO` LIKE '%"
                    + this.tBoxRegiao.getText() + "%' AND `STATUS` LIKE '%"
                    + this.tBoxStatus.getText() + "%' AND `TIPO` LIKE '%"
                    + this.tBoxTipo.getText() + "%' AND `GESTOR_SUB` LIKE '%"
                    + this.tBoxGestorSub.getText() + "%' AND `GESTOR_RG` LIKE '%"
                    + this.tBoxGestorReg.getText() + "%' AND `GESTOR_DIV` LIKE '%"
                    + this.tBoxGestorDiv.getText() + "%' ORDER BY `FILIAL`");
            ResultSet rs = st.getResultSet();

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("ID"),
                    rs.getString("FILIAL"),
                    rs.getString("REGIAO"),
                    rs.getString("TIPO"),
                    rs.getString("STATUS"),
                    rs.getString("GESTOR_SUB"),
                    rs.getString("GESTOR_RG"),
                    rs.getString("GESTOR_DIV")});
            }
            ConexaoMySQL.FecharConexao();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }
    }

