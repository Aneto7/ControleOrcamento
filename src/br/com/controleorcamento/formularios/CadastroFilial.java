    private void bAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtualizarActionPerformed

        Filial filial = new Filial();
        FilialAtualizar filialatu = new FilialAtualizar();

        filial.setId(Integer.parseInt(lID.getText()));
        filial.setFilial(tBoxFilial.getText());
        filial.setRegiao(tBoxRegiao.getText());
        filial.setStatus(tBoxStatus.getText());
        filial.setTipo(tBoxTipo.getText());
        filial.setGestorsub(tBoxGestorSub.getText());
        filial.setGestorrg(tBoxGestorReg.getText());
        filial.setGestordiv(tBoxGestorDiv.getText());

        filialatu.FilialAtualizar(filial);
        limpar();
        filtrar();
        JOptionPane.showMessageDialog(this, "Filial atualizada com sucesso");
    }//GEN-LAST:event_bAtualizarActionPerformed

