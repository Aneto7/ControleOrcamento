    private void tabelaVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaVeiculosMouseClicked
        if (tabelaVeiculos.getSelectedRow() != -1) {
            lID.setText(tabelaVeiculos.getValueAt(tabelaVeiculos.getSelectedRow(), 0).toString());
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = ConexaoMySQL.getConexaoMySQL();
                java.sql.Statement st = conn.createStatement();
                st.executeQuery("SELECT * FROM `filial` WHERE `ID` = " + this.lID.getText());
                ResultSet rs = st.getResultSet();

                while (rs.next()) {

                    this.tBoxFilial.setText(rs.getString("FILIAL"));
                    this.tBoxRegiao.setText(rs.getString("REGIAO"));
                    this.tBoxStatus.setText(rs.getString("STATUS"));
                    this.tBoxGestorSub.setText(rs.getString("GESTOR_SUB"));
                    this.tBoxGestorReg.setText(rs.getString("GESTOR_RG"));
                    this.tBoxGestorDiv.setText(rs.getString("GESTOR_DIV"));
                    this.tBoxTipo.setText(rs.getString("TIPO"));
                }
                ConexaoMySQL.FecharConexao();
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, e);
                ConexaoMySQL.FecharConexao();
            }
        }
    }//GEN-LAST:event_tabelaVeiculosMouseClicked

