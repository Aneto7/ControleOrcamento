    private void bExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirActionPerformed
        if (lID.getText() == "") {
            JOptionPane.showMessageDialog(this, "Selecione uma filial clicando na tabela");
            return;
        }
        Filial filial = new Filial();
        FilialExcluir filialexc = new FilialExcluir();

        filial.setId(Integer.parseInt(lID.getText()));

        filialexc.FilialExcluir(filial);
        limpar();
        filtrar();
        JOptionPane.showMessageDialog(this, "Filial excluída com sucesso");
    }//GEN-LAST:event_bExcluirActionPerformed

