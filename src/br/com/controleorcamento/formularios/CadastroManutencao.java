    private void bAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtualizarActionPerformed
        Manutencao manut = new Manutencao();
        ManutencaoAtualizar manutatu = new ManutencaoAtualizar();
        SimpleDateFormat fordata = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date datainicio = fordata.parse((String) this.fTextDataInicio.getText());
            Date datafim = fordata.parse((String) this.fTextDataFim.getText());
            SimpleDateFormat forma = new SimpleDateFormat("yyyy-MM-dd");
            String datainiciofor = (String) forma.format(datainicio);
            String datafimfor = (String) forma.format(datafim);
            manut.setDatainicio(datainiciofor);
            manut.setDatafim(datafimfor);
        } catch (ParseException ex) {
            Logger.getLogger(CadastroManutencao.class.getName()).log(Level.SEVERE, null, ex);
        }
        String valortransformado = ((String) this.fTextValor.getText());
        valortransformado = valortransformado.replace(".", "");
        valortransformado = valortransformado.replace(",", ".");

        manut.setId(Integer.parseInt(lID.getText()));
        manut.setPlaca((String) cBoxPlaca.getSelectedItem());
        manut.setGrupomanutencao((String) cBoxGrupoManutencao.getSelectedItem());
        manut.setValor(Double.parseDouble(valortransformado));
        manut.setResponsavel(lResponsavel.getText());
        manut.setOrcamento((String) cBoxOrçamento.getSelectedItem());
        manut.setDetalhe(tAreaDetalhe.getText());
        
        if (this.checkBoxCarroReserva.isSelected()) {
            manut.setCarroreserva(true);
        } else {
            manut.setCarroreserva(false);
        }

        manutatu.ManutencaoAtualizar(manut);
        limpar();
        filtrar();
        JOptionPane.showMessageDialog(this, "Manutenção atualizada com sucesso");
    }//GEN-LAST:event_bAtualizarActionPerformed

