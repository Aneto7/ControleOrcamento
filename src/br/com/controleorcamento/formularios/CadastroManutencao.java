    private void cBoxPlacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cBoxPlacaActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT DISTINCT CONDUTOR FROM `veiculos` WHERE `PLACA` LIKE '%"
                    + this.cBoxPlaca.getSelectedItem() + "' ORDER BY CONDUTOR");
            ResultSet rs = st.getResultSet();
            while (rs.next()) {
                lResponsavel.setText(rs.getString("CONDUTOR"));
            }
            ConexaoMySQL.FecharConexao();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }
    }//GEN-LAST:event_cBoxPlacaActionPerformed

