    private void tabelaManutencaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaManutencaoMouseClicked
        if (tabelaManutencao.getSelectedRow() != -1) {
            lID.setText(tabelaManutencao.getValueAt(tabelaManutencao.getSelectedRow(), 0).toString());
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = ConexaoMySQL.getConexaoMySQL();
                java.sql.Statement st = conn.createStatement();
                st.executeQuery("SELECT * FROM `manutencao` WHERE `ID` = " + this.lID.getText());
                ResultSet rs = st.getResultSet();

                while (rs.next()) {
                    SimpleDateFormat fordata = new SimpleDateFormat("yyyy-MM-dd");
                    DecimalFormat df = new DecimalFormat("0.00");

                    try {
                        Date datainicio = fordata.parse((String) rs.getString("DATA_INICIO"));
                        Date datafim = fordata.parse((String) rs.getString("DATA_FIM"));
                        SimpleDateFormat forma = new SimpleDateFormat("dd/MM/yyyy");
                        String datainiciofor = (String) forma.format(datainicio);
                        String datafimfor = (String) forma.format(datafim);

                        this.fTextDataInicio.setValue(datainiciofor);
                        this.fTextDataFim.setValue(datafimfor);

                    } catch (ParseException ex) {
                        Logger.getLogger(CadastroManutencao.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    this.cBoxPlaca.setSelectedItem(rs.getString("PLACA"));
                    this.cBoxGrupoManutencao.setSelectedItem(rs.getString("GRUPO_MANUTENCAO"));
                    this.fTextValor.setText(df.format(rs.getDouble("VALOR")));
                    this.lResponsavel.setText(rs.getString("RESPONSAVEL"));
                    this.cBoxOrçamento.setSelectedItem(rs.getString("ORCAMENTO"));
                    this.tAreaDetalhe.setText(rs.getString("DETALHE"));
                                        
                    if (rs.getBoolean("CARRO_RESERVA") == false) {
                        this.checkBoxCarroReserva.setSelected(false);
                    } else {
                        this.checkBoxCarroReserva.setSelected(true);
                    }
                }
                ConexaoMySQL.FecharConexao();
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, e);
                ConexaoMySQL.FecharConexao();
            }
        }
    }//GEN-LAST:event_tabelaManutencaoMouseClicked

