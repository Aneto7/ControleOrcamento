    public void filtrar() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaManutencao.getModel();
        tabelaManutencao.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT * FROM `manutencao` WHERE `PLACA` LIKE '%"
                    + this.cBoxPlaca.getSelectedItem() + "%' AND `GRUPO_MANUTENCAO` LIKE '%"
                    + this.cBoxGrupoManutencao.getSelectedItem() + "%' AND `ORCAMENTO` LIKE '%"
                    + this.cBoxOrçamento.getSelectedItem() + "%' ORDER BY `PLACA`");
            ResultSet rs = st.getResultSet();

            while (rs.next()) {
                SimpleDateFormat formatado = new SimpleDateFormat("dd/MM/yyyy");
                String inicioformat = formatado.format(rs.getDate("DATA_INICIO"));
                String fimformat = formatado.format(rs.getDate("DATA_FIM"));

                modelo.addRow(new Object[]{
                    rs.getInt("ID"),
                    rs.getString("PLACA"),
                    inicioformat,
                    fimformat,
                    rs.getString("GRUPO_MANUTENCAO"),
                    rs.getDouble("VALOR"),
                    rs.getString("RESPONSAVEL"),
                    rs.getString("ORCAMENTO"),
                    rs.getString("CARRO_RESERVA"),});
            }
            ConexaoMySQL.FecharConexao();

        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }//Fim try
    }

    public void limpar() {
        this.cBoxPlaca.setSelectedItem("");
        this.cBoxGrupoManutencao.setSelectedItem("");
        this.lResponsavel.setText("");
        this.fTextDataInicio.setText("");
        this.fTextDataFim.setText("");
        this.fTextValor.setText("0");
        this.cBoxOrçamento.setSelectedItem("");
        this.checkBoxCarroReserva.setSelected(false);
        this.tAreaDetalhe.setText("");
    }

    public void carregarPlaca() {
        this.cBoxPlaca.removeAllItems();
        this.cBoxPlaca.addItem("");
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT DISTINCT PLACA FROM `veiculos` ORDER BY PLACA");
            ResultSet rs = st.getResultSet();
            while (rs.next()) {
                cBoxPlaca.addItem(rs.getString("PLACA"));
            }
            ConexaoMySQL.FecharConexao();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }
    }

