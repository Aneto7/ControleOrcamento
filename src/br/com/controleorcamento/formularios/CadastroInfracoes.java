    public void somarValoresdaTabela() {
        DecimalFormat df = new DecimalFormat("###,###,###,###.##");
        df.setRoundingMode(RoundingMode.UP);

        double somamultas = 0;
        double somapontos = 0;
        for (int i = 0; i <= tabelaInfracoes.getRowCount() - 1; i++) {
            somamultas += Double.parseDouble(tabelaInfracoes.getValueAt(i, 6).toString());
            somapontos += Double.parseDouble(tabelaInfracoes.getValueAt(i, 9).toString());
        }
        lValorMultas.setText(String.valueOf(df.format(somamultas)));
        lPontosMultas.setText(String.valueOf(df.format(somapontos)));
    }

