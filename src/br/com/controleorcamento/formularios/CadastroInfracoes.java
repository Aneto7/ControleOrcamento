    private void tabelaInfracoesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaInfracoesMouseClicked
        if (tabelaInfracoes.getSelectedRow() != -1) {
            lID.setText(tabelaInfracoes.getValueAt(tabelaInfracoes.getSelectedRow(), 0).toString());
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = ConexaoMySQL.getConexaoMySQL();
                java.sql.Statement st = conn.createStatement();
                st.executeQuery("SELECT * FROM `infracoes` WHERE `ID` = " + this.lID.getText());
                ResultSet rs = st.getResultSet();

                while (rs.next()) {
                    SimpleDateFormat fordata = new SimpleDateFormat("yyyy-MM-dd");
                    DecimalFormat df = new DecimalFormat("0.00");

                    try {
                        Date datainfra = fordata.parse((String) rs.getString("DATA"));
                        Date prazorec = fordata.parse((String) rs.getString("PRAZO_RECURSO"));
                        SimpleDateFormat forma = new SimpleDateFormat("dd/MM/yyyy");
                        String datainfrafor = (String) forma.format(datainfra);
                        String prazorecfotor = (String) forma.format(prazorec);

                        this.fTextPrazo.setValue(prazorecfotor);
                        this.fTextDataInfracao.setValue(datainfrafor);

                    } catch (ParseException ex) {
                        Logger.getLogger(CadastroInfracoes.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    this.cBoxPlaca.setSelectedItem(rs.getString("PLACA"));
                    this.cBoxLocadora.setSelectedItem(rs.getString("LOCADORA"));
                    this.cBoxPontos.setSelectedItem(rs.getString("PONTOS"));
                    this.lGravidade.setText(rs.getString("GRAVIDADE"));
                    this.tBoxCodigo.setText(rs.getString("CODIGO"));
                    this.tBoxDescricaoMulta.setText(rs.getString("DESCRICAO"));
                    this.fTextValor.setText(df.format(rs.getDouble("VALOR")));
                    this.tBoxAutoInfracao.setText(rs.getString("AUTO_DE_INFRACAO"));
                    this.cBoxCondutor.setSelectedItem(rs.getString("CONDUTOR"));
                    this.tBoxNChamado.setText(rs.getString("N_CHAMADO"));
                    this.cBoxCondutorIdentificado.setSelectedItem(rs.getString("CONDUTOR_IDENTIFICADO"));
                    this.tBoxDescricaoMulta.setText(rs.getString("OBSERVACAO"));

                    if (rs.getBoolean("REEMBOLSO") == false) {
                        this.checkBoxReembolso.setSelected(false);
                    } else {
                        this.checkBoxReembolso.setSelected(true);
                    }
                }
                ConexaoMySQL.FecharConexao();
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, e);
                ConexaoMySQL.FecharConexao();
            }
        }
    }//GEN-LAST:event_tabelaInfracoesMouseClicked

