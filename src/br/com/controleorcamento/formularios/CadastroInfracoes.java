    public void filtrar() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaInfracoes.getModel();
        tabelaInfracoes.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT * FROM `infracoes` WHERE `PLACA` LIKE '%"
                    + this.cBoxPlaca.getSelectedItem() + "%' AND `LOCADORA` LIKE '%"
                    + this.cBoxLocadora.getSelectedItem() + "%' AND `GRAVIDADE` LIKE '%"
                    + this.lGravidade.getText() + "%' AND `CODIGO` LIKE '%"
                    + this.tBoxCodigo.getText() + "%' AND `AUTO_DE_INFRACAO` LIKE '%"
                    + this.tBoxAutoInfracao.getText() + "%' AND `CONDUTOR` LIKE '%"
                    + this.cBoxCondutor.getSelectedItem() + "%' AND `N_CHAMADO` LIKE '%"
                    + this.tBoxNChamado.getText() + "%' AND `CONDUTOR_IDENTIFICADO` LIKE '%"
                    + this.cBoxCondutorIdentificado.getSelectedItem() + "%' ORDER BY `CONDUTOR`");
            ResultSet rs = st.getResultSet();

            while (rs.next()) {
                SimpleDateFormat formatado = new SimpleDateFormat("dd/MM/yyyy");
                String prazo = formatado.format(rs.getDate("PRAZO_RECURSO"));

                modelo.addRow(new Object[]{
                    rs.getInt("ID"),
                    rs.getString("PLACA"),
                    rs.getString("AUTO_DE_INFRACAO"),
                    rs.getString("LOCADORA"),
                    rs.getString("GRAVIDADE"),
                    rs.getString("CODIGO"),
                    rs.getDouble("VALOR"),
                    prazo,
                    rs.getString("CONDUTOR"),
                    rs.getInt("PONTOS"),});
            }
            somarValoresdaTabela();
            ConexaoMySQL.FecharConexao();

        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }
    }

