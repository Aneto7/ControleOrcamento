    private void tabelaCartaoCarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaCartaoCarMouseClicked
        if (tabelaCartaoCar.getSelectedRow() != -1) {
            lID.setText(tabelaCartaoCar.getValueAt(tabelaCartaoCar.getSelectedRow(), 0).toString());
            carregarVeiculo();
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = ConexaoMySQL.getConexaoMySQL();
                java.sql.Statement st = conn.createStatement();
                st.executeQuery("SELECT * FROM `cartoes_ticketcar` WHERE `ID` = " + this.lID.getText());
                ResultSet rs = st.getResultSet();

                while (rs.next()) {
                    this.cBoxVeiculo.setSelectedItem(rs.getString("VEICULO"));
                    this.tBoxCartao.setText(rs.getString("CARTAO"));
                    this.cBoxDescricao.setSelectedItem(rs.getString("DESCRICAO"));
                    this.tBoxResponsavel.setText(rs.getString("RESPONSAVEL"));
                    this.cBoxStatus.setSelectedItem(rs.getString("STATUS"));
                    this.tBoxCentroCusto.setText(rs.getString("CENTRO_DE_CUSTO"));
                }
                ConexaoMySQL.FecharConexao();
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, e);
                ConexaoMySQL.FecharConexao();
            }
        }
    }//GEN-LAST:event_tabelaCartaoCarMouseClicked

