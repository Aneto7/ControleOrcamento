    private void bAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtualizarActionPerformed

        CartaoCar cart = new CartaoCar();
        CartaoCarAtualizar catu = new CartaoCarAtualizar();

        cart.setId(Integer.parseInt(lID.getText()));
        cart.setCartao(tBoxCartao.getText());
        cart.setVeiculo((String) cBoxVeiculo.getSelectedItem());
        cart.setDescricao((String) cBoxDescricao.getSelectedItem());
        cart.setResponsavel(tBoxResponsavel.getText());
        cart.setStatus((String) cBoxStatus.getSelectedItem());
        cart.setCentro_de_custo(tBoxCentroCusto.getText());

        catu.CartaoCarAtualizar(cart);
        limpar();
        filtrar();
        JOptionPane.showMessageDialog(this, "Cartão do Carro atualizado com sucesso");
    }//GEN-LAST:event_bAtualizarActionPerformed

    private void bSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT VEICULO FROM `cartoes_ticketcar` WHERE `VEICULO` LIKE '"
                    + this.cBoxVeiculo.getSelectedItem() + "'");
            ResultSet rs = st.getResultSet();
            if (rs.first()) {
                JOptionPane.showMessageDialog(this, "Veículo já possui cartão cadastrado ou cartão esta cadastrado para outro veículo");
                return;
            }
            CartaoCar cart = new CartaoCar();
            CartaoCarSalvar csalv = new CartaoCarSalvar();

            cart.setCartao(tBoxCartao.getText());
            cart.setVeiculo((String) cBoxVeiculo.getSelectedItem());
            cart.setDescricao((String) cBoxDescricao.getSelectedItem());
            cart.setResponsavel(tBoxResponsavel.getText());
            cart.setStatus((String) cBoxStatus.getSelectedItem());
            cart.setCentro_de_custo(tBoxCentroCusto.getText());

            csalv.CartaoCarSalvar(cart);
            limpar();
            filtrar();
            JOptionPane.showMessageDialog(this, "Cartão de Carro cadastrado com sucesso");
            ConexaoMySQL.FecharConexao();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }


    }//GEN-LAST:event_bSalvarActionPerformed

    private void bExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirActionPerformed
        if (lID.getText() == "") {
            JOptionPane.showMessageDialog(this, "Selecione um Cartão clicando na tabela");
            return;
        }
        CartaoCar cart = new CartaoCar();
        CartaoCarExcluir cexc = new CartaoCarExcluir();

        cart.setId(Integer.parseInt(lID.getText()));

        cexc.CartaoCarExcluir(cart);
        limpar();
        filtrar();
        JOptionPane.showMessageDialog(this, "Cartão de veículo excluído com sucesso");
    }//GEN-LAST:event_bExcluirActionPerformed

