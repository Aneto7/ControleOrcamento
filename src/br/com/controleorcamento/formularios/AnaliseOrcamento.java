    private void tabelaOrcamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaOrcamentoMouseClicked
        DecimalFormat df = new DecimalFormat("###,###,###,##0.00");
        df.setRoundingMode(RoundingMode.UP);

        if (tabelaOrcamento.getSelectedRow() != -1) {
            lID.setText(tabelaOrcamento.getValueAt(tabelaOrcamento.getSelectedRow(), 0).toString());
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = ConexaoMySQL.getConexaoMySQL();
                java.sql.Statement st = conn.createStatement();
                st.executeQuery("SELECT * FROM `orcamentocontrole` WHERE `ID` = " + this.lID.getText());
                ResultSet rs = st.getResultSet();
                while (rs.next()) {
                    this.cBoxPlataforma.setSelectedItem(rs.getString("SEGMENTO"));
                    this.cBoxRegiao.setSelectedItem(rs.getString("REGIAO"));
                    this.cBoxFilial.setSelectedItem(rs.getString("DESCRICAO_FILIAL"));
                    this.cBoxMagnitude.setSelectedItem(rs.getString("MAGNITUDE_CONTA"));
                    this.cBoxCentroCusto.addItem(rs.getString("CENTRO_DE_CUSTO"));
                    this.cBoxCentroCusto.setSelectedItem(rs.getString("CENTRO_DE_CUSTO"));
                    this.cBoxConta.addItem(rs.getString("CONTA"));
                    this.cBoxConta.setSelectedItem(rs.getString("CONTA"));
                    this.cBoxNomeUsuario.setSelectedItem(rs.getString("RESPONSAVEL"));
                }
                this.jTextArea1.setText("INSERIR ANÁLISE");
                filtrarAnalise();
                ConexaoMySQL.FecharConexao();
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, e);
                ConexaoMySQL.FecharConexao();
            }
        }
    }//GEN-LAST:event_tabelaOrcamentoMouseClicked

