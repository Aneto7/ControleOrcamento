    public void diferencaMes() {
        DecimalFormat df = new DecimalFormat("###,###,###,##0.00");
        df.setRoundingMode(RoundingMode.UP);
        int count = tabelaOrcamento.getRowCount();
        for (int row = count - 1; row >= 0; row--) {
            Double realmes = (Double) tabelaOrcamento.getValueAt(row, 10);
            Double bdgmes = (Double) tabelaOrcamento.getValueAt(row, 9);
            String dm = String.valueOf(df.format(bdgmes - realmes));
            dm = dm.replace(".", "");
            dm = dm.replace(",", ".");
            Double difmes = Double.parseDouble(dm);
            tabelaOrcamento.setValueAt(difmes, row, 11);

            Double realacum = (Double) tabelaOrcamento.getValueAt(row, 13);
            Double bdgacum = (Double) tabelaOrcamento.getValueAt(row, 12);
            String da = String.valueOf(df.format(bdgacum - realacum));
            da = da.replace(".", "");
            da = da.replace(",", ".");
            Double difacum = Double.parseDouble(da);
            tabelaOrcamento.setValueAt(difacum, row, 14);
        }
    }

