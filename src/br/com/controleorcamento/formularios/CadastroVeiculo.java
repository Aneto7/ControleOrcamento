    public void filtrar() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaVeiculos.getModel();
        tabelaVeiculos.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = ConexaoMySQL.getConexaoMySQL();
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("SELECT * FROM `veiculos` WHERE `PLACA` LIKE '%"
                    + this.tBoxPlaca.getText() + "%' AND `LOCADOR` LIKE '%"
                    + this.tBoxLocador.getText() + "%' AND `UNIDADE` LIKE '%"
                    + this.cBoxUnidade.getSelectedItem() + "%' AND `MODELO` LIKE '%"
                    + this.cBoxModelo.getSelectedItem() + "%' AND `TIPO` LIKE '%"
                    + this.cBoxTipo.getSelectedItem() + "%' AND `CLASSIFICACAO` LIKE '%"
                    + this.cBoxClassificacao.getSelectedItem() + "%' AND `EMPRESA` LIKE '%"
                    + this.cBoxEmpresa.getSelectedItem() + "%' AND `CARTAO` LIKE '%"
                    + this.tBoxCartao.getText() + "%' AND `CONDUTOR` LIKE '%"
                    + this.tBoxCondutor.getText() + "%' AND `DEVOLVIDO` LIKE '%"
                    + this.cBoxStatus.getSelectedItem() + "%' ORDER BY `PLACA`");
            ResultSet rs = st.getResultSet();

            while (rs.next()) {
                SimpleDateFormat formatado = new SimpleDateFormat("dd/MM/yyyy");
                String inicioformat = formatado.format(rs.getDate("INICIO_CONTRATO"));
                String fimformat = formatado.format(rs.getDate("FIM_CONTRATO"));

                modelo.addRow(new Object[]{
                    rs.getInt("ID"),
                    rs.getString("PLACA"),
                    rs.getString("LOCADOR"),
                    rs.getDouble("VALOR_LOCACAO"),
                    rs.getString("UNIDADE"),
                    rs.getString("EMPRESA"),
                    inicioformat,
                    fimformat,
                    rs.getString("TIPO"),
                    rs.getString("MODELO"),
                    rs.getString("CLASSIFICACAO"),
                    rs.getString("CARTAO"),
                    rs.getString("CONDUTOR")});
            }
            ConexaoMySQL.FecharConexao();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
            ConexaoMySQL.FecharConexao();
        }
    }

